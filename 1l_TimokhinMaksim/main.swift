//Задание 2. Работа с треугольником (efg)
//Опредилим катеты
var e:Double = 3.0
var f:Double = 4.0
//Найдем гипотенузу по теореме Пифагора
var g:Double = (sqrt((pow(e, 2))+(pow(f, 2)))) //гипотенуза
//Найдем периметр
var P = e+f+g
// Найдем площадь
var p = P/2 //полупериметр
var S = (sqrt(p*((p-e)*(p-f)*(p-g)))) //формула Герона
print("Гипотенуза = \(g), Периметр = \(P), Площадь = \(S)")

