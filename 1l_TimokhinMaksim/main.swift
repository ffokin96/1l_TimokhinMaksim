// Решение предлагаю через общую формулу нахождения корней квадратного уравнения при D>=0
// (решение в комплексной плоскоти не рассматриваю так как не знаю поведение среды при работе с мниными единицами (если кто знает буду рад увидеть!)
// Опредилим дискриминант
var D:Double = (pow(b,2))-(4*a*c)
// Найдем корни уравнения
